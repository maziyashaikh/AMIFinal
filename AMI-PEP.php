            <p style="font-size:18px;text-align:center;font-weight:400;letter-spacing:0px;color:#4a4a4a;"><b>Shri Ashwini Vaishnaw, Hon'ble Minister of Electronics and Information Technology, Communications and Railways, recently announced the 'a' app, a People Empowerment platform, built by Axis My India in partnership with Google. The ‘a’ app is being built with the Purpose of empowering citizens with authentic information and solutions based on their needs and aspirations.</b></p>
<!--<p style="font-size:20px;text-align:left;font-weight:400;letter-spacing:0px;color:black;">Read more on this : <a href:"https://goo.gle/3Q6yFeA"</a>https://goo.gle/3Q6yFeA</p>-->
<p style="font-size:20px;text-align:left;font-weight:400;letter-spacing:0px;color:black;">Read more on this : <a href="https://goo.gle/3Q6yFeA">https://goo.gle/3Q6yFeA</a></p>
 <p style="line-height:24px;letter-spacing:0px; color:#4a4a4a;text-align:justify;color:black;font-size:20px;">India’s leading consumer data intelligence company, Axis My India, today announced that it is building a one-of-a-kind platform leveraging Google technology that aims to bring about a step change in people’s awareness, accessibility, and utilization of a slew of services. These include Government social welfare schemes, basic day-to-day amenities, employment opportunities, and healthcare benefits, and has the potential to benefit up to 250 million Indian households across the country.</p>
<br>
 <p style="line-height:24px;letter-spacing:0px; color:#4a4a4a;text-align:justify;color:black;font-size:20px;">Built on Google Cloud’s scalable and secure infrastructure, Axis My India’s People Empowerment Platform (PEP) application will use Google Cloud’s generative AI (GenAI) technology to provide relevant information about government schemes and desired communication/content to citizens based on their individual and family needs. In order to be useful to people across geographies, the PEP will be designed to support 13 regional languages employing Google translation API and Vertex AI for real-time translation and transliteration of information. This two-way communication will facilitate enhanced engagement with Axis My India’s Mission <b>‘To Actively Contribute Every day to Transform a Billion Lives’</b> by acting as a digital and physical ‘Jan Suvidha Kendra’ (Public Welfare Centre). The roll-out of PEP will be supported by Axis My India’s network of local offices in more than 700 districts across India.</p>
 <br>
 <p style="line-height:24px;letter-spacing:0px; color:#4a4a4a;text-align:justify;color:black;font-size:20px;">Speaking on the partnership, <b>Pradeep Gupta, Chairman and Managing Director, 
Axis My India,</b> said, “Our collaboration with Google marks a pivotal moment in our pursuit to positively impact the lives of a billion Indians. By leveraging Google Cloud technologies, we aim to create an inclusive platform that truly empowers citizens with authentic information and solutions based on their needs. Axis My India’s massive scale and expertise affords us an unprecedented opportunity to bring information equity between rural and urban India through our platform. We are certain that we will achieve our ambitious goal of acting as a digital super-app for all 250 million Indian households.”
</p>
<p style="line-height:24px;letter-spacing:0px; color:#4a4a4a;text-align:justify;color:black;font-size:20px;">“Google Cloud offers a wide range of services that can benefit business in terms of scalability, flexibility, security, performance, innovation and support. Rising mobile connectivity presents the opportunity to create an inclusive, equitable, and safer internet for everyone, no matter their literacy, language, or location. We are excited to work with Axis My India to help advance their vision with the People Empowerment Platform, backed by state-of-the-art Gen AI Cloud technologies from Google Cloud. This will help them bring government services and programs to more Indians across the country," said <b>Bikram Singh Bedi, Managing Director, Google Cloud India.</b>
</p>
<p style="line-height:24px;letter-spacing:0px; color:#4a4a4a;text-align:justify;color:black;font-size:20px;">Axis My India announced their vision for PEP in 2021 in line with its Mission Himalaya – Unchi Udaan Roadmap Initiative and is currently in the advanced stages of developing this with Google, with the pilot version of the PEP app expected to launch in October 2023. 
</p>

